const ROCK = 1;
const PAPER = 2;
const SCISSORS = 3;

function RockPaperScissors(player1, player2) {
	const diff = player1 - player2;
	if (diff === 0) return "Draw";
	if (diff === -1 || diff === 2) return "Player 2 wins";
	if (diff === 1 || diff === -2) return "Player 1 wins";
}

function letterToNumber(letter) {
	if (letter === "A" || letter === "X") return ROCK;
	if (letter === "B" || letter === "Y") return PAPER;
	if (letter === "C" || letter === "Z") return SCISSORS;
}

const parsed = input.split("\n").map((line) => {
	const [player1, player2] = line.split(" ");
	return { p1: letterToNumber(player1), p2: letterToNumber(player2) };
});

let totalPoints = 0;
parsed.forEach(({ p1, p2 }) => {
	const result = RockPaperScissors(p1, p2);
	if (result === "Player 2 wins") totalPoints += 6;
	if (result === "Draw") totalPoints += 3;
	if (p2 === ROCK) totalPoints += 1;
	if (p2 === PAPER) totalPoints += 2;
	if (p2 === SCISSORS) totalPoints += 3;
});

